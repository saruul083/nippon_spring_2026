package month02.week01.day04;

public class Whileloop {
    public static void main(String[] args) {
        int i = 0;
        while (i < 5) {
            System.out.println(i + " ");
            i++;
        }
        i = 2;
        while (i < 9) {
            System.out.println(i + "");
            i = i + 2;
        }
        System.out.println();
    }
}
